#include <stdlib.h>
#include <string.h>
#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	size_t	len;

	len = count * size;
	ptr = (char *)malloc(len);
	if (ptr)
	{
		ft_memset(ptr, 0, len);
		return (ptr);
	}
	return (NULL);
}
