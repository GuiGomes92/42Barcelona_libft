#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	len;
	int	i;

	len = ft_strlen(s);
	i = 0;
	while (i <= len)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
		i++;
	}
	return (0);
}
